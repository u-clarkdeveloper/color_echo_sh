#!/bin/bash
header () {
    echo -e '\033[1;32m'
    echo -e "${1}\n-------------------------------------------------------------\033[0m"
}
title () {
    echo -e '\033[1;36m'
    echo $1
    echo "------------------------------------------------------------------"
    echo -e '\033[0m'
}
footer () {
    echo -e '\033[1;36m'
    echo "------------------------------------------------------------------"
    echo -e "${1}\033[0m"
}

error () {
    echo -e '\033[1;31m'
    echo -e "${1}\033[0m"
}

warning () {
    echo -e '\033[1;33m'
    echo -e "${1}\033[0m"
}
info () {
    echo -e '\033[1;30m'
    echo -e "${1}\033[0m"
}
good () {
    echo -e '\033[1;32m'
    echo -e "${1}\033[0m"
}
highlight () {
    echo -e '\033[1;37m'
    echo -e "${1}\033[0m"
}